using System;

namespace Lacos {
   class Program {
      static void Main(string[] args) {
         
         // Execução do laço de repetição 'for'
         for (int a = 10; a < 20; a = a + 1) {
            Console.WriteLine("Valor de a: {0}", a);
         }
         Console.ReadLine();
      }
   }
} 