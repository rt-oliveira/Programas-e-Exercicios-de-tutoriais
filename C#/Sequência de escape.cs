using System;

namespace CaractereEscape{
	class Program{
		static void Main(string[] args){
			/* Nessa impressão são vistos 2 sequências de escape, \n e \t, 
				que respectivamente indicam nova linha (quebra de linha) e um TAB.*/
			Console.WriteLine("Hello\tWorld\n\n");
			Console.ReadLine();
		}
	}
}