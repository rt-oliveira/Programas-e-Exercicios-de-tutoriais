-- Função SIGN
-- Mostra o sinal de um determinado número.
-- Se a função retornar:
--  1  - Significa que é um número poitivo
--  0  - Significa que é o número 0.
--  -1 - Significa que é um número negativo

-- Agora são vistos 3 usos da função, para identificar
-- os sinais de 3 números diferentes.
SELECT SIGN(-4.65); -- Retorna -1

SELECT SIGN(0);  -- Retorna 0

SELECT SIGN(4.65);  -- Retorna 1