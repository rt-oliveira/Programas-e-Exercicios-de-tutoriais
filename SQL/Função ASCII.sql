-- Função ASCII

-- Retorna o valor ASCII do caractere mais a esquerda da string.
-- Se for passada uma string vazia, retorna 0.
-- Se for passado NULL, retorna NULL.

-- Neste exemplo, retorna o valor ASCII do caractere '2'.
SELECT ASCII('2');

-- Já neste outro exemplo, retorna o valor ASCII do primeiro caractere
-- da string, que é o 'd'.
SELECT ASCII('dx');