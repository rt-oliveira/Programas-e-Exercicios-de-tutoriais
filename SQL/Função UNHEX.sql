-- Função UNHEX
-- Dada uma string hexadecimal (aquela só com caracteres numéricos e de 'a' a 'f'),
-- converterá cada par de caracteres hexadecimais para um caractere ASCII, formando
-- uma string ASCII.

-- Neste exemplo, a string hexadecimal '4D7953514C' será convertida em 'MySQL'.
SELECT UNHEX('4D7953514C');