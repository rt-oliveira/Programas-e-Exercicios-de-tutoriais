-- Função FIELD
-- Dada uma primeira string (S) e uma lista de strings (L), a função retornará
-- o índice da S em L (começando em 1).
-- Caso não encontre, a função retorna 0.

-- Neste exemplo, é mostrado que a string 'ej' é a segunda da lista de strings
-- passadas.
SELECT FIELD('ej','Hej','ej','Heja','hej','foo');
