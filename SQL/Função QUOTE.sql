-- Função QUOTE
-- Dado uma string S, essa função retornará S entre
-- aspas simples.

-- Essa função pode ser boa para gerar resultados que
-- podem ser usados em comandos SQL.

-- Neste exemplo, dado a string passada como parâmetro,
-- será retornado a mesma entre aspas simples.
SELECT QUOTE('Don\'t');
-- Aqui foi passado (Don\'t) e voltará ('Don\'t').